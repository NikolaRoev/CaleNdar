//Public:
void UI::application_loop() {
	serialize(years);
	
	bool open = true;
	while (open) {
		switch (cn::APPLICATION_STATE) {
			case START_MENU: {
				start_menu_loop();
				break;
			}
			case DAY_SELECTION_MENU: {
				day_selection_menu_loop();
				break;
			}
			case INDIVIDUAL_DAY_MENU: {
				individual_day_loop();
				break;
			}
			case EXIT: {
				serialize(years);
				open = false;
				break;
			}
		}
	}
}

